    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed

        try {
            
            PreparedStatement pps = cn.prepareStatement("INSERT INTO usuario(Nombre, Apellidos, Edad, Correo) VALUES(?, ?, ?, ?)");
            pps.setString(1, txt_nombre.getText().trim());
            pps.setString(2, txt_apellidos.getText().trim());
            pps.setString(3, txt_edad.getText().trim());
            pps.setString(4, txt_correo.getText().trim());
            pps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Datos guardados.");
            mostrartabla();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error.");
        }

    }//GEN-LAST:event_botonGuardarActionPerformed

