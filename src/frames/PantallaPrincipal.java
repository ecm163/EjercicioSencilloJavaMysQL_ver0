    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        
        int fila = tabla_datos.getSelectedRow();
        if(fila >= 0){
            
            txt_buscar.setText(tabla_datos.getValueAt(fila, 0).toString());
            txt_nombre.setText(tabla_datos.getValueAt(fila, 1).toString());
            txt_apellidos.setText(tabla_datos.getValueAt(fila, 2).toString());
            txt_edad.setText(tabla_datos.getValueAt(fila, 3).toString());
            txt_correo.setText(tabla_datos.getValueAt(fila, 4).toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "Fila no seleccionada.");
        }
        
    }//GEN-LAST:event_botonModificarActionPerformed

    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE usuario SET Nombre = '"+txt_nombre.getText()+
                    "' ,Apellidos = '"+txt_apellidos.getText()+"' ,Edad = '"+txt_edad.getText()+
                    "' ,Correo ='"+txt_correo.getText()+"' WHERE Id = "+txt_buscar.getText()+"");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Actualizados.");
            limpiar();
            mostrartabla();
            
        } catch (SQLException ex) {
            Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        
        int fila = tabla_datos.getSelectedRow();
        String valor = tabla_datos.getValueAt(fila, 0).toString();
        if(fila >= 0){
            
            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM usuario WHERE Id = '"+valor+"'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato eliminado.");
                mostrartabla();
            } catch (SQLException ex) {
                Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else{
            
            
            
        }
        
    }//GEN-LAST:event_botonEliminarActionPerformed

